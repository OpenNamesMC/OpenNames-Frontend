<script>
import { getCookie, setCookie } from 'tiny-cookie'
// import nav from '../Navbar.vue'

export default {
    data: function() {
        return {
            currentTheme: 'dark'
        }
    },
    created() {
        // check if cookie for darkmode is set - if yes, apply it!
        let value = getCookie("theme")
        if (value == "light") {
            this.applyLight()
        } else if (value == "dark") {
            this.applyDark()
        } else {
            setCookie('theme', 'dark', { expires: '1D', SameSite: 'strict' });
            this.applyDark()
        }
    },
    methods: {
        toggleTheme: function() {
            if (this.currentTheme == "dark") {
                this.applyLight()
                // nav.methods.toggleLogo('light')
                this.$emit("changeTheme", "light");
            } else {
                this.applyDark()
                // nav.methods.toggleLogo('dark')
                this.$emit("changeTheme", "dark");
            }
        },
        applyDark: function() {
            let doc = document.getElementsByTagName("body")[0]
            doc.className = "dark bg-gray-800";
            this.currentTheme = "dark"
            setCookie('theme', 'dark', { expires: '1D', SameSite: 'strict' });
        },
        applyLight: function() {
            let doc = document.getElementsByTagName("body")[0]
            doc.className = "light bg-white";
            this.currentTheme = "light"
            setCookie('theme', 'light', { expires: '1D', SameSite: 'strict' });
        },
    }
}
</script>

